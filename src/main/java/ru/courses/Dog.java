package ru.courses;

/**
 * Класс описывает домашнее животное: собака
 */
public class Dog extends Pet{

    public Dog(String name) {
        super(name);
    }

    /**
     * @inheritDoc
     */
    @Override
    public void makeSound() {
        System.out.println("Гав!");
    }

    @Override
    public String toString() {
        return "собака " + getName();
    }
}
